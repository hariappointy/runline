#!/usr/bin/env node

/**
 * Generates the plugin table for README.md from built plugin metadata.
 *
 * Usage:
 *   node scripts/generate-plugin-table.js          # print to stdout
 *   node scripts/generate-plugin-table.js --write   # update README.md in place
 */

import { readFileSync, writeFileSync } from "node:fs";
import { discoverPlugins } from "../dist/plugin/loader.js";

const plugins = await discoverPlugins(null);
plugins.sort((a, b) => a.name.localeCompare(b.name));

const lines = [];
lines.push(`| Plugin | Actions | Resources | Auth |`);
lines.push(`|--------|--------:|-----------|------|`);

for (const p of plugins) {
  const resources = [...new Set(p.actions.map((a) => a.name.split(".")[0]))];
  const resourceList = resources.join(", ");

  const authFields = p.connectionConfigSchema
    ? Object.entries(p.connectionConfigSchema)
        .map(([key, field]) => (field.env ? `\`${field.env}\`` : `\`${key}\``))
        .join(", ")
    : "—";

  lines.push(
    `| **${p.name}** | ${p.actions.length} | ${resourceList} | ${authFields} |`,
  );
}

const table = lines.join("\n");

if (process.argv.includes("--write")) {
  const readme = readFileSync("README.md", "utf-8");
  const start = "<!-- BEGIN PLUGIN TABLE -->";
  const end = "<!-- END PLUGIN TABLE -->";
  const startIdx = readme.indexOf(start);
  const endIdx = readme.indexOf(end);

  if (startIdx < 0 || endIdx < 0) {
    console.error(
      "Could not find <!-- BEGIN PLUGIN TABLE --> / <!-- END PLUGIN TABLE --> markers in README.md",
    );
    process.exit(1);
  }

  const updated =
    readme.slice(0, startIdx) +
    `${start}\n<!-- Generated by: node scripts/generate-plugin-table.js -->\n${table}\n${end}` +
    readme.slice(endIdx + end.length);

  writeFileSync("README.md", updated);
  console.log(`Updated README.md with ${plugins.length} plugins.`);
} else {
  console.log(table);
}
